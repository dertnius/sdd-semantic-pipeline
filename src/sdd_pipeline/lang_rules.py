"""
Per-language enrichment rule packs.

This module is the **single place** language-specific enrichment data lives. Each
supported language contributes one :class:`LangPack` (keyed by ISO 639-1 code) and
nothing else in the codebase hardcodes a natural language. ``enrichment.py`` compiles
these packs into matchers (cached per language); ``retrieval.py`` reads ``snowball_name``
for optional stemming.

Adding a 5th language is a **data-only** change: append one ``LangPack(...)`` literal to
:data:`_PACKS` and its code to :data:`SUPPORTED_LANGUAGES`. No logic anywhere else needs
to change — the classifiers, extractors, tokenizers and detector are all
language-parameterised, and the Unicode ``\\w`` tokenizers already cover Latin-script
diacritics.

Design notes (mirroring ``enrichment.py``'s contract):
- ``section_rules`` / ``genre_title_rules`` keywords are matched with a **leading word
  boundary only** (``\\b`` before the keyword, none after), so a keyword matches at a word
  *start* and still catches inflections/compounds — e.g. German ``"sicherheit"`` matches
  ``"Sicherheitskonzept"``; English ``"config"`` matches ``"configuration"``. Keep keywords
  specific so they are not a prefix of an unrelated word.
- Section-rule **order matters** (first match wins); keep the :class:`SectionType` ordering
  identical across languages.
- This module imports only :mod:`.models` (pure enums) — it stays I/O-free so
  ``enrichment.py`` can keep its no-I/O guardrail.
"""

from __future__ import annotations

from dataclasses import dataclass

from .models import Genre, SectionType

# Tech/protocol acronyms are language-neutral; every language's SECURITY/API lists
# share them so a "JWT"/"OAuth" heading classifies the same regardless of prose language.
_SHARED_SECURITY_ACRONYMS = ["token", "rbac", "jwt", "oauth", "saml", "tls"]
# Admonition callout words frequently used verbatim (``> [!NOTE]``) even in non-English
# docs, so every pack folds the English labels in alongside its own.
_EN_ADMONITIONS = ("note", "warning", "tip", "important", "caution", "info", "danger", "attention")


@dataclass(frozen=True)
class LangPack:
    """All language-specific enrichment data for one language.

    Stored as plain data (lists/sets/pattern bodies); ``enrichment.py`` compiles the
    regex matchers from it, cached per language.
    """

    code: str
    section_rules: dict[SectionType, list[str]]
    genre_title_rules: dict[Genre, list[str]]
    # Inner alternation body for strong obligation modals (POLICY genre signal); compiled
    # as ``\b(?:<modal_pattern>)\b`` with IGNORECASE. Weak modals (en "should", de "soll")
    # are intentionally excluded — too common in ordinary explanation.
    modal_pattern: str
    imperative_verbs: frozenset[str]
    rake_stopwords: frozenset[str]
    admonition_labels: tuple[str, ...]
    # snowballstemmer language name (en→"english", …) or None if unsupported.
    snowball_name: str | None


