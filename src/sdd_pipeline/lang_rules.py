_PACKS: dict[str, LangPack] = {
    "en": _EN,
    "de": _DE,
    "fr": _FR,
    "it": _IT,
}

#: ISO 639-1 codes with a rule pack. The language detector's allowed set and the CLI's
#: ``--lang`` choices derive from this, so adding a pack widens both automatically.
SUPPORTED_LANGUAGES: tuple[str, ...] = tuple(_PACKS)

DEFAULT_LANGUAGE = "en"


def normalize_language(language: str | None) -> str:
    """Return a supported ISO code for *language*, falling back to ``en``.

    Accepts e.g. ``"de"``, ``"DE"``, ``"de-DE"``, ``"de_AT"`` → ``"de"``. An unknown or
    empty value degrades to :data:`DEFAULT_LANGUAGE` so a typo never crashes enrichment.
    """
    if not language:
        return DEFAULT_LANGUAGE
    code = language.strip().lower().replace("_", "-").split("-", 1)[0]
    return code if code in _PACKS else DEFAULT_LANGUAGE


def get_lang_pack(language: str | None) -> LangPack:
    """Return the :class:`LangPack` for *language* (normalised, ``en`` fallback)."""
    return _PACKS[normalize_language(language)]
